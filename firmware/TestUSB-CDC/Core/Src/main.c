/* CRC */
uint8_t SHT45_CRC(uint8_t *data, uint8_t len)
{
    uint8_t crc = 0xFF;

    for (uint8_t i = 0; i < len; i++)
    {
        crc ^= data[i];
        for (uint8_t j = 0; j < 8; j++)
        {
            crc = (crc & 0x80) ? (crc << 1) ^ 0x31 : (crc << 1);
        }
    }
    return crc;
}

/* Odczyt czujnika */
HAL_StatusTypeDef SHT45_Read(float *temp, float *hum)
{
    uint8_t cmd = 0xFD; // high precision
    uint8_t rx[6];

    if (HAL_I2C_Master_Transmit(&hi2c1, SHT45_ADDR, &cmd, 1, 100) != HAL_OK)
        return HAL_ERROR;

    HAL_Delay(10);

    if (HAL_I2C_Master_Receive(&hi2c1, SHT45_ADDR, rx, 6, 100) != HAL_OK)
        return HAL_ERROR;

    if (SHT45_CRC(&rx[0], 2) != rx[2]) return HAL_ERROR;
    if (SHT45_CRC(&rx[3], 2) != rx[5]) return HAL_ERROR;

    uint16_t rawT = (rx[0] << 8) | rx[1];
    uint16_t rawH = (rx[3] << 8) | rx[4];

    *temp = -45.0f + 175.0f * ((float)rawT / 65535.0f);
    *hum  = -6.0f  + 125.0f * ((float)rawH / 65535.0f);

    return HAL_OK;
}

